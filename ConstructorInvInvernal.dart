import 'package:practica2/ConstructorInvernadero.dart';
import 'package:practica2/EstructuraInvernadero.dart';
import 'package:practica2/Iluminacion.dart';
import 'package:practica2/Macetero.dart';
import 'package:practica2/TanqueAgua.dart';
import 'package:practica2/Calefaccion.dart';
import 'package:practica2/PlantadorVertical.dart';

class ConstructorInvInvernal extends ConstructorInvernadero {

      void aniadeEstructura() => invernadero.setEstructura(new EstructuraInvernadero(true));

      void aniadeIluminacion() => invernadero.setIluminacion(new Iluminacion());

      void aniadeMaceteros() => invernadero.setMaceteros(new Macetero());

      void aniadeTanque() => invernadero.setTanqueAgua(new FuenteCiudad(100.0));

      void aniadeControlTemperatura() => invernadero.setControlTemperatura(new Calefaccion());

      void aniadePlantador() => invernadero.setPlantador(new PlantadorVertical());
  
}